def process_species(species_name: str, in_folder: str, out_folder: str) -> int:
    os.makedirs(out_folder, exist_ok=True)

    audio_files = (list(Path(in_folder).glob("*.mp3")) +
                   list(Path(in_folder).glob("*.wav")))
    if not audio_files:
        print(f"  âš ï¸  No audio in {in_folder}")
        return 0

    chunk_samples = int(CHUNK_SEC * SR)
    chunk_count   = 0

    for audio_path in tqdm(audio_files, desc=f"  {species_name}", leave=False):
        try:
            y, _ = librosa.load(str(audio_path), sr=SR, mono=True)

            # Remove leading and trailing silence.
            y, _ = librosa.effects.trim(y, top_db=TOP_DB)

            if len(y) < MIN_CHUNK_DUR * SR:
                continue

            # Split into non-overlapping chunks.
            chunks = []
            for start in range(0, len(y) - chunk_samples + 1, chunk_samples):
                chunk = y[start : start + chunk_samples]
                if len(chunk) == chunk_samples:
                    chunks.append(chunk)

            # Pad if clip is shorter than one full chunk.
            if not chunks and len(y) >= MIN_CHUNK_DUR * SR:
                padded            = np.zeros(chunk_samples, dtype=np.float32)
                padded[:len(y)]  = y
                chunks.append(padded)

            for chunk in chunks:
                # Save original chunk spectrogram.
                fname = f"{species_name}_orig_{chunk_count:06d}.png"
                audio_to_melspec_image(
                    chunk, SR, os.path.join(out_folder, fname)
                )
                chunk_count += 1

                # Save augmented chunk spectrograms.
                for aug_idx in range(AUG_PER_CHUNK):
                    aug = augment_audio(chunk, SR)

                    # Restore exact chunk length after augmentation.
                    if len(aug) >= chunk_samples:
                        aug = aug[:chunk_samples]
                    else:
                        pad            = np.zeros(chunk_samples, dtype=np.float32)
                        pad[:len(aug)] = aug
                        aug            = pad

                    fname = f"{species_name}_aug{aug_idx}_{chunk_count:06d}.png"
                    audio_to_melspec_image(
                        aug, SR, os.path.join(out_folder, fname)
                    )
                    chunk_count += 1

        except Exception as e:
            print(f"    âŒ  {audio_path.name}: {e}")

    return chunk_count

