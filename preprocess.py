# AUGMENTATION
# ============================================================

def augment_audio(y: np.ndarray, sr: int) -> np.ndarray:
    """
    Apply a random subset of augmentations.
    Mild enough that spectrograms still look like real bird calls.
    """
    aug = y.copy()

    # 1. Time stretch +/-15%
    if random.random() < 0.6:
        rate = random.uniform(0.85, 1.15)
        aug  = librosa.effects.time_stretch(aug, rate=rate)

    # 2. Pitch shift +/-2 semitones
    if random.random() < 0.6:
        steps = random.uniform(-2.0, 2.0)
        aug   = librosa.effects.pitch_shift(aug, sr=sr, n_steps=steps)

    # 3. Add Gaussian background noise
    if random.random() < 0.5:
        noise_amp = random.uniform(0.001, 0.008)
        aug = aug + noise_amp * np.random.randn(len(aug))

    # 4. Random gain variation
    if random.random() < 0.5:
        gain = random.uniform(0.5, 1.5)
        aug  = aug * gain

    # 5. Low-coefficient pre-emphasis
    if random.random() < 0.3:
        cutoff = random.randint(100, 500)   # Hz
        aug    = librosa.effects.preemphasis(aug, coef=cutoff / sr)

    aug = np.clip(aug, -1.0, 1.0)
    return aug

# ============================================================
# SPECTROGRAM - FIXED NORMALIZATION
# ============================================================

def audio_to_melspec_image(y: np.ndarray, sr: int, save_path: str):
    """
    Converts waveform â†’ Mel-spectrogram PNG.

    Uses FIXED vmin/vmax so every image is on the same colour scale.
    This is critical: per-sample normalisation causes the model to
    treat a loud recording and a quiet recording identically, which
    destroys relative amplitude information.
    """
    mel    = librosa.feature.melspectrogram(
        y=y, sr=sr, n_mels=N_MELS, n_fft=N_FFT, hop_length=HOP_LENGTH
    )
    mel_db = librosa.power_to_db(mel, ref=np.max)
    # Keep dB range consistent across all spectrograms.


    mel_db = np.clip(mel_db, MEL_DB_MIN, MEL_DB_MAX)

    fig, ax = plt.subplots(
        figsize=(IMG_SIZE[1] / 100, IMG_SIZE[0] / 100), dpi=100
    )
    ax.imshow(
        mel_db,
        aspect="auto",
        origin="lower",         # Low frequencies at bottom
        cmap="viridis",
        vmin=MEL_DB_MIN,        # Fixed scale
        vmax=MEL_DB_MAX,        # Fixed scale
        interpolation="nearest"
    )
    ax.axis("off")
    fig.subplots_adjust(left=0, right=1, top=1, bottom=0)
    fig.savefig(save_path, dpi=100, bbox_inches="tight", pad_inches=0)
    plt.close(fig)

